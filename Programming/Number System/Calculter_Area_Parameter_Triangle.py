# Area and Perimeter of a Rectangle
# Take length and width as input and calculate area and perimeter.

length=float(input("Enter length: "))
width=float(input("Enter width: "))
calculate_Area=float(length*width)
calculate_Perimeter=float((length*width)/2)
print("Area of triangle where length is ",length, " and width is ",width, " =",calculate_Area)
print("Perimeter of triangle where length is ",length, " and width is ",width, " =",calculate_Perimeter)